     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $review = new Review();
        $review->nama_reviewer = $request->NamaReview;
        $review->reviewer = $request->Review;
        $review->film_id =$request->FilmId;
        $review->save();

        return redirect('/list');
    }

